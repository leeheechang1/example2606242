bullet("음성 비서인 Siri 또는 Google Assistant")
bullet("고객 상담 챗봇")
bullet("교육용 AI 튜터")
body("이러한 자연스러운 상호작용은 기술을 더 쉽고 편리하게 사용할 수 있도록 돕는다.")

h3("2.5 목표 지향성(Goal-Oriented Behavior)")
