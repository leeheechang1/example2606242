body("기술이 발전하면서 AI 에이전트는 사람들의 생활 방식, 학습 방법, 업무 환경, 그리고 의사소통 방식까지 변화시키고 있다.")
body("이 글에서는 AI 에이전트의 주요 특징과 우리 삶에 미치는 영향을 설명한다.")

