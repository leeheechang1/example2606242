# -*- coding: utf-8 -*-
from fpdf import FPDF

pdf = FPDF(format='A4')
pdf.add_font("Malgun", style="", fname="C:/Windows/Fonts/malgun.ttf")
pdf.add_font("Malgun", style="B", fname="C:/Windows/Fonts/malgunbd.ttf")
pdf.set_auto_page_break(auto=True, margin=15)
pdf.set_left_margin(20)
pdf.set_right_margin(20)
pdf.set_top_margin(20)
pdf.add_page()

W = pdf.epw  # effective page width

def title(text):
    pdf.set_font("Malgun", "B", 18)
    pdf.multi_cell(W, 10, text)
    pdf.ln(4)

def h2(text):
    pdf.set_font("Malgun", "B", 13)
    pdf.multi_cell(W, 8, text)
    pdf.ln(2)

def h3(text):
    pdf.set_font("Malgun", "B", 11)
    pdf.multi_cell(W, 7, text)
    pdf.ln(1)

def body(text):
    pdf.set_font("Malgun", "", 10)
    pdf.multi_cell(W, 6, text)
    pdf.ln(2)

def bullet(text):
    pdf.set_font("Malgun", "", 10)
    pdf.multi_cell(W, 6, "- " + text)

